async function seed() {
  console.log('Seeding T12 serendipity translations…');
  let count = 0;
  for (const [locale, keys] of Object.entries(translations)) {
    for (const [key, value] of Object.entries(keys)) {
      await sql`
        INSERT INTO translations (locale, key, value, namespace)
        VALUES (${locale}, ${key}, ${value}, 'ui')
        ON CONFLICT (locale, key) DO UPDATE SET value = EXCLUDED.value
      `;
      count++;
    }
  }
  console.log(`Done. ${count} rows upserted.`);
}

seed().catch(console.error);
